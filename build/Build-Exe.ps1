# Build-Exe.ps1  - compiles My Clean PC into a portable .exe using PS2EXE
# Usage: powershell -ExecutionPolicy Bypass -File build\Build-Exe.ps1 [-Version "1.0.0"]
param([string]$Version = "1.0.0")

$root      = Split-Path $PSScriptRoot -Parent
$scriptsDir = Join-Path $root "scripts"
$distDir    = Join-Path $root "dist"
$merged     = Join-Path $distDir "MyCleanPC-Merged.ps1"
$outExe     = Join-Path $distDir "MyCleanPC-Portable.exe"

# Ensure dist folder exists
if (-not (Test-Path $distDir)) { New-Item -ItemType Directory -Path $distDir -Force | Out-Null }

# -- Merge core + launcher into one file -------------------------------------
$core     = Get-Content (Join-Path $scriptsDir "clean-pc-core.ps1") -Raw
$launcher = Get-Content (Join-Path $scriptsDir "my-clean-pc.ps1")   -Raw

# Strip the dot-source line from the launcher (core is now inlined)
$launcher = $launcher -replace '(?m)^\. \$corePath\s*$', ''
$launcher = $launcher -replace '(?m)^\$corePath\s*=.*$', ''
$launcher = $launcher -replace '(?m)^if \(-not \(Test-Path \$corePath\)\).*?exit 1\s*}', '' -replace '(?smi)if \(-not \(Test-Path \$corePath\)\)[^}]+}', ''

$merged_content = @"
# My Clean PC - merged portable build v$Version
# Generated by build\Build-Exe.ps1 - DO NOT EDIT DIRECTLY

$core

$launcher
"@

Set-Content -Path $merged -Value $merged_content -Encoding UTF8
Write-Host "Merged script written to: $merged"

# -- Compile with PS2EXE ------------------------------------------------------
if (-not (Get-Command Invoke-PS2EXE -ErrorAction SilentlyContinue)) {
    Write-Host "Installing PS2EXE..." -ForegroundColor Yellow
    Install-Module ps2exe -Scope CurrentUser -Force -AllowClobber
}

Invoke-PS2EXE `
    -InputFile  $merged `
    -OutputFile $outExe `
    -NoConsole:$false `
    -RequireAdmin `
    -Title       "My Clean PC" `
    -Description "Silent, prompt-free Windows cache cleaner" `
    -Company     "Rajkumar" `
    -Version     $Version `
    -IconFile    (Join-Path $root "electron\assets\icon.ico")

if (Test-Path $outExe) {
    $hash = (Get-FileHash $outExe -Algorithm SHA256).Hash
    "$hash  MyCleanPC-Portable.exe" | Set-Content "$outExe.sha256"
    Write-Host ""
    Write-Host "Built: $outExe" -ForegroundColor Green
    Write-Host "SHA256: $hash"  -ForegroundColor Cyan
} else {
    Write-Error "Build failed - output exe not found."
    exit 1
}
